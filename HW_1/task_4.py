"""
Напишите программу, которая по заданному номеру четверти, показывает диапазон возможных координат точек
в этой четверти (x и y).

Ввод: значение типа <int>
Вывод: значение типа <str>

Пример:

3
x < 0, y < 0
"""

n = int(input('Введите номер четверти от 1 до 4: '))
if n == 1:
    print('x > 0, y > 0')
elif n == 2:
    print('x < 0, y > 0')
elif n == 3:
    print('x < 0, y < 0')
elif n == 4:
    print('x > 0, y < 0')
else:
    print('Введите корректное число от 1 до 4!')
